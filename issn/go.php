	$nodes[] = 'node [id ' . $i . ' label "' .  join(' ', $obj->rows[$i]->key) . '"]';
}


$i = 9; $j = 14;
$debug = true;
$debug = false;

for ($i = 1; $i < $n; $i++)
{
	//echo $i . ' ' . join(' ', $obj->rows[$i]->key);
	//echo "\n";
	
	for ($j = 0; $j < $i; $j++)
	{
		//echo $j . ' ' . join(' ', $obj->rows[$j]->key) . ' ';
	
		$str1 = join(' ', $obj->rows[$i]->key);		
		$str2 = join(' ', $obj->rows[$j]->key);
		
		$lcs = new LongestCommonSequence($str1, $str2);
		
		$d = $lcs->score();
		
		$p = $d / min(strlen($str1), strlen($str2));
		
		$lcs->get_alignment();
		
		
		
		if ($debug)
		{
			echo "\n";
			echo $lcs->left . "\n";
			echo $lcs->bars . "\n";
			echo $lcs->right . "\n";
			echo "$d characters match\n";
			echo "$p of shortest string matches\n";
			echo "\n";
		}
		
		$threshold = 1.0;
		$threshold = 0.9;
		
		if ($p >= $threshold)
		{
		
			$parts = array();
		
			$part = new stdclass;
			$part->l = '';
			$part->r = '';
		
			$alignment_length = strlen($lcs->left);
			for ($k = 0; $k < $alignment_length; $k++)
			{
				if ($lcs->left{$k} == ' ' || $lcs->right{$k} == ' ')
				{
					$parts[] = $part;
				
					$part = new stdclass;
					$part->l = '';
					$part->r = '';
			
				}
				else
				{			
					$part->l .= $lcs->left{$k};
					$part->r .= $lcs->right{$k};
				}
			
		
			}
			$parts[] = $part;
		
			if ($debug)
			{
				print_r($parts);
			}
		
			// score parts
		
			$score = 0;
			foreach ($parts as $part)
			{
				$part->l  = str_replace('-', '', $part->l);
				$part->r  = str_replace('-', '', $part->r);
			
				if (strlen($part->l) > 0 &&  strlen($part->r) > 0)
				{
			
					// exact match?
					if (strcmp($part->l, $part->r) === 0)
					{
						$score += 1.1;
					}
					else
					{
					
						// one is left substring of the other
						$pattern = '';
						if (strlen($part->l) > strlen($part->r))
						{
							if (preg_match('/^' . $part->r . '/', $part->l))
							{
								$score += 1;
							}
						}
						else
						{
							if (preg_match('/^' . $part->l . '/', $part->r))
							{
								$score += 1;
							}
						}
					
					}
			
				}
		
			}
		
			//echo "score=$score\n";
		
			$shortest = min(count($obj->rows[$i]->key), count($obj->rows[$j]->key));
		
			//echo "score=$shortest\n";
		
			if ($score > 1 && $score >= $shortest)
			{
				$edges[] = 'edge [ source ' . $i . ' target ' . $j . ' label "' . $score . '"]';
			}
			
			//$edges[] = 'edge [ source ' . $i . ' target ' . $j . ' label "' . $score . '"]';
		}		
		
		/*
		echo $lcs->score() . "\n";
		echo "\n";
		$lcs->show_alignment();
		echo "\n";
		*/
	}
	//echo "\n";
}



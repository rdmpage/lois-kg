// Delete a work with a DOI
function delete_work($doi)
{
	global $couch;
	
	$id = 'https://doi.org/' . $doi;
	
	$exists = $couch->exists($id);

	if ($exists)
	{
		echo "Have $id\n";
		
		$couch->add_update_or_delete_document(null, $id, 'delete');
	}
	else
	{
		echo "Echo $id doesn't exist\n";
	}
}


//----------------------------------------------------------------------------------------
function crossref_fetch($doi)
{
	global $couch;
	global $force;
	
	$id = 'https://doi.org/' . $doi;
	
	$exists = $couch->exists($id);

	$go = true;
	if ($exists && !$force)
	{
		echo "Have already\n";
		$go = false;
	}

	if ($go)
	{
		$doc = get_work($doi);
		
		//print_r($doc);
		
		
		if ($doc)
		{
		
			if (!$exists)
			{
				$couch->add_update_or_delete_document($doc, $doc->_id, 'add');	
			}
			else
			{
				if ($force)
				{
					$couch->add_update_or_delete_document($doc, $doc->_id, 'update');
				}
			}
		}
	}	
}



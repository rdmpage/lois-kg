function ipni_author_fetch($id)
{
	global $couch;
	global $force;
	
	$id = 'urn:lsid:ipni.org:authors:' . $id;
	
	$exists = $couch->exists($id);

	$go = true;
	if ($exists && !$force)
	{
		echo "Have already\n";
		$go = false;
	}

	if ($go)
	{
		$url = 'https://beta.ipni.org/api/1/a/' . $id;
		
		echo $url . "\n";
		
		$json = get($url);
		
		echo $json;
		
		$doc = json_decode($json);
		$doc->_id = $id;

		if ($doc)
		{
		
			if (!$exists)
			{
				$couch->add_update_or_delete_document($doc, $doc->_id, 'add');	
			}
			else
			{
				if ($force)
				{
					$couch->add_update_or_delete_document($doc, $doc->_id, 'update');
				}
			}
		}
	}	
}

